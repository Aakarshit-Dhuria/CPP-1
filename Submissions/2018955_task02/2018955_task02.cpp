#include<bits/stdc++.h>

using namespace std;

int main()
{
  //Computing the size of different data type by using sizeof function
  
  cout<<"Memory Size of int is "<<sizeof(int)<<endl;
                                         
  cout<<"Memory Size of char is "<<sizeof(char)<<endl;
                                         
  cout<<"Memory Size of float is "<<sizeof(float)<<endl;
                                         
  cout<<"Memory Size of long is "<<sizeof(long)<<endl;
                                         
  cout<<"Memory Size of double is "<<sizeof(double)<<endl;
  
  return 0;
  
}
